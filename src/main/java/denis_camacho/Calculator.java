package denis_camacho;

public class Calculator {

    /**
     * This method performs the sum of two numbers.
     *
     * @param firstNumber  double value.
     * @param secondNumber double values.
     * @return double result of the sum.
     */
    public double sum(double firstNumber, double secondNumber) {
        return firstNumber + secondNumber;
    }

    /**
     * This method performs the subtraction of two numbers.
     *
     * @param firstNumber  double value.
     * @param secondNumber double values.
     * @return double result of the subtraction.
     */
    public double subtraction(double firstNumber, double secondNumber) {
        return firstNumber - secondNumber;
    }

    /**
     * This method performs the division of two numbers.
     *
     * @param firstNumber  double value.
     * @param secondNumber double values.
     * @return double result of the division.
     */
    public double division(double firstNumber, double secondNumber) {
        return firstNumber / secondNumber;
    }

    /**
     * This method performs the multiplication of two numbers.
     *
     * @param firstNumber  double value.
     * @param secondNumber double values.
     * @return double result of the multiplication.
     */
    public double multiplication(double firstNumber, double secondNumber) {
        return firstNumber * secondNumber;
    }
}
