package main;

/**
 * Class calcultatro has add,
 * substrac,divide adn multiply methods.
 */
public class calculator {
    /**
     *
     * @param a first value
     * @param b second value
     * @return summation
     */
    public int add(int a, int b) {
        return a + b;
    }
    /**
     *
     * @param a first value
     * @param b second value
     * @return difference
     */
    public int subtrac(int a, int b) {
        return a - b;
    }
    /**
     *
     * @param a first value
     * @param b second value
     * @return divide
     */
    public int divide(int a, int b) {
        return a / b;
    }
    /**
     *
     * @param a first value
     * @param b second value
     * @return product
     */
    public int multiply(int a, int b) {
        return a * b;
    }
}
